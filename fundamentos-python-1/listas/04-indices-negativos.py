# INDICES NEGATIVOS 

# Um elemento com um índice igual a -1 é o último na lista.

numbers = [111, 7, 2, 1]
print(numbers[-2])          # O snippet de exemplo gerará 1. Execute o programa e verifique.



# Da mesma forma, o elemento com um índice igual a -2 é o penúltimo na lista .

numbers = [111, 7, 2, 1]
print(numbers[-2])          # O snippet de exemplo gerará 2.

